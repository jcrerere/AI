const normalizeRaceName = (value?: string): string => {
  const raw = `${value || ''}`.trim();
  if (!raw) return '人类';
  const text = raw.replace(/^灵族[_-]?/, '').replace(/^种族[:：]\s*/, '');
  if (/溶灵|史莱姆/.test(text)) return '史莱姆';
  const knownRaces = ['汐屿族', '月光精灵', '魅魔', '狐魅', '戈尔贡', '塞壬', '花妖', '蚁女', '蜘蛛新娘', '史莱姆', '人类'];
  const matched = knownRaces.find(race => text.includes(race));
  return matched || text || '人类';
};

