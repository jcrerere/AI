export const getSpeciesProfile = (race?: string): SpeciesProfile | null => {
  if (!`${race || ''}`.trim()) return null;
  const normalized = normalizeRaceName(race);
  return SPECIES_PROFILES[normalized] || null;
};

export const getSpeciesTraits = (race?: string): SpeciesTrait[] => getSpeciesProfile(race)?.traits || [];

export const getSpeciesDerivedItemTemplates = (race?: string): SpeciesDerivedItemTemplate[] => {
  if (!`${race || ''}`.trim()) return [];
  const normalized = normalizeRaceName(race);
  const ids = SPECIES_ITEM_ORDER[normalized] || [];
  return ids.map(id => SPECIES_ITEM_LIBRARY[id]).filter(Boolean);
};

export const estimateItemMarketValue = (
  item: Pick<Item, 'id' | 'name' | 'rank' | 'category'> & { commodityProfile?: ItemCommodityProfile },
  locationLabel?: string,
): number => {
  const commodityProfile = item.commodityProfile;
  const basePrice = commodityProfile?.basePrice ?? RANK_BASE_PRICE[item.rank] ?? 120;
  const marketCategory = commodityProfile?.marketCategory ?? DEFAULT_MARKET_CATEGORY[item.category];
  const marketTier = commodityProfile?.marketTier ?? 'standard';
  const rarityFactor = commodityProfile?.rarity ? RARITY_FACTOR[commodityProfile.rarity] : 1;
  const legalFactor =
    commodityProfile?.legalStatus === '违禁' ? 1.12 : commodityProfile?.legalStatus === '灰市' ? 1.05 : 1;

  return buildRegionalRetailPrice({
    basePrice,
    category: marketCategory,
    locationLabel,
    tier: marketTier,
    discountTier: 0,
    seedText: `item_value|${item.id}|${item.name}|${locationLabel || 'default'}`,
    extraFactor: rarityFactor * legalFactor,
  });
};

export const materializeSpeciesItem = (
  templateId: string,
  options?: {
    id?: string;
    quantity?: number;
  },
): Item | null => {
  const template = SPECIES_ITEM_LIBRARY[templateId];
  if (!template) return null;
  return {
    id: options?.id || `species_${template.id}`,
    name: template.name,
    quantity: Math.max(1, options?.quantity || 1),
    icon: template.icon,
    description: template.description,
    category: template.category,
    rank: template.rank,
    commodityProfile: { ...template.commodityProfile },
  };
};

